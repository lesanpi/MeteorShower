public class Punto {
    private float x,y;

    public Punto(float x, float y) {
        this.x=x;
        this.y=y;
    }

    public float getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }
}
